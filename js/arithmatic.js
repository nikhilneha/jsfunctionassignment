function addition(num1,num2){
    return num1+num2;
}
console.log('addition--'+addition(65,46));

function substraction(num1,num2){
    return num1-num2;
}
console.log('Substraction--'+substraction(69,34));

function multiplication(num1,num2){
    return num1*num2;
}
console.log('multiplication--'+multiplication(25,6));

function division(num1,num2){
    return num1/num2;
}
console.log('division--'+division(156,16));